    private void updateTable() {
    String[] columnNames = {"No", "Product Name", "Bar Code", "Cost Price", "Sell Price", "Quantity", "Supplier ID", "Supplier Name", "Supplier TP Number"};
    DefaultTableModel model = new DefaultTableModel(columnNames, 0);
    
    try {
        String query = "SELECT * FROM dbo.product";
        Statement s = DBUtil.getConnection().createStatement();
        ResultSet result = s.executeQuery(query);
        
        while (result.next()) {
            Object[] row = {
                result.getInt("id"),                    
                result.getString("product_name"),
                result.getString("bar_code"),
                result.getBigDecimal("cost_price"),     
                result.getBigDecimal("sell_price"),     
                result.getInt("qty"),
                result.getInt("supplier_id"),
                result.getString("supplier_name"),
                result.getString("supplier_tpnumber")
            };
            model.addRow(row);
        }
    } catch (SQLException e) {
        System.out.println("Error occurred while fetching product data: " + e.getMessage());
    }
    jTable1.setModel(model);
}
    
