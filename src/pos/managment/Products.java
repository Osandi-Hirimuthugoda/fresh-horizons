    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Update Btn:
        String product_name = p_name.getText();
        String bar_code = p_barcode.getText();
        String cost_price = p_costprice.getText();
        String sell_price = p_sellprice.getText();
        String qty = p_qty.getText();
        String Supplier_id = p_supplierid.getText();
        String Supplier_name = p_suppliername1.getText();
        String Supplier_tpnumber = p_tpnumber.getText();
        int id = Integer.parseInt(p_searchid.getText());

        if (bar_code == null || bar_code.trim().isEmpty()) {
            System.out.println("Product barcode cannot be empty. Update aborted.");
            return; 
        }

        try {
            String sql = "UPDATE dbo.product SET product_name = ?, bar_code = ?, cost_price = ?, sell_price = ?, qty = ?, supplier_id = ?, supplier_name = ?, supplier_tpnumber = ? WHERE id = ?";

            PreparedStatement s = DBUtil.getConnection().prepareStatement(sql);
            s.setString(1, product_name);
            s.setString(2, bar_code);
            s.setString(3, cost_price);
            s.setString(4, sell_price);
            s.setString(5, qty);
            s.setString(6, Supplier_id);
            s.setString(7, Supplier_name);
            s.setString(8, Supplier_tpnumber);
            s.setInt(9, id);
            
            

            int rowsUpdated = s.executeUpdate();
            if (rowsUpdated > 0) {
                System.out.println("Product details updated successfully.");
            } else {
                System.out.println("No product found with the given ID.");
            }

        } catch (SQLException e) {
            System.out.println("Error executing SQL update: " + e.getMessage());
        } catch (NumberFormatException e) {
            System.out.println("Invalid product ID format: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Delete Btn
        String id = p_searchid.getText();
    
        if (id == null || id.trim().isEmpty()) {
            System.out.println("product ID cannot be empty. Deletion aborted.");
            return; // Stop execution if ID is empty
        }

        try {
           
            String sql = "DELETE FROM product WHERE id = ?";
            PreparedStatement s = DBUtil.getConnection().prepareStatement(sql);
            s.setString(1, id);  

            
            int rowsDeleted = s.executeUpdate();

            if (rowsDeleted > 0) {
                System.out.println("Product with ID " + id + " deleted successfully.");
            } else {
                System.out.println("No product found with the given ID.");
            }

        } catch (SQLException e) {
            System.out.println("Error executing SQL delete: " + e.getMessage());
            System.out.println(e);
        }   
    }//GEN-LAST:event_jButton1ActionPerformed


